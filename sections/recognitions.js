// "See More" Functionality
// Wait for DOM to update
setTimeout(() => {
    const recognitionCards = document.querySelectorAll('.recognition-card');
    const seeMoreBtn = document.getElementById('see-more-recognitions');
    const itemsToShow = 9;

    // Initially hide items beyond the limit
    recognitionCards.forEach((card, index) => {
        if (index >= itemsToShow) {
            card.style.display = 'none';
        }
    });

    // If total items are less than or equal to limit, hide the button
    if (recognitionCards.length <= itemsToShow) {
        seeMoreBtn.style.display = 'none';
    }

    seeMoreBtn.addEventListener('click', () => {
        const isHidden = recognitionCards[itemsToShow].style.display === 'none';

        if (isHidden) {
            // Show all items
            recognitionCards.forEach(card => {
                card.style.display = 'flex'; // Restore flex display
                card.style.animation = 'fadeIn 0.5s ease forwards';
            });
            seeMoreBtn.textContent = 'Show Less';
        } else {
            // Hide items again
            recognitionCards.forEach((card, index) => {
                if (index >= itemsToShow) {
                    card.style.display = 'none';
                }
            });
            seeMoreBtn.textContent = 'See More';

            // Scroll back to the recognitions section to avoid being lost
            document.getElementById('recognitions').scrollIntoView({ behavior: 'smooth' });
        }
    });
}, 0);
